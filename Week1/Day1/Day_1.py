# 导入 math 库以使用 power (幂) 等数学运算
import math

# 设置爱心的密度和大小，通过循环生成坐标系
for y in range(15, -15, -1):
    line = ""
    for x in range(-30, 30):
        # 心形代数方程: (x^2 + y^2 - 1)^3 - x^2 * y^3 = 0
        # 这里对坐标进行了缩放 (0.05, 0.1) 以适应字符显示的比例
        f = ( (x * 0.05)**2 + (y * 0.1)**2 - 1 )**3 - (x * 0.05)**2 * (y * 0.1)**3
        
        # 如果点在曲线内部 (f <= 0)，打印字符，否则打印空格
        line += "love"[(x - y) % 4] if f <= 0 else " "
    print(line)